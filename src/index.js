const MORSE_TABLE = {
  ".-": "a",
  "-...": "b",
  "-.-.": "c",
  "-..": "d",
  ".": "e",
  "..-.": "f",
  "--.": "g",
  "....": "h",
  "..": "i",
  ".---": "j",
  "-.-": "k",
  ".-..": "l",
  "--": "m",
  "-.": "n",
  "---": "o",
  ".--.": "p",
  "--.-": "q",
  ".-.": "r",
  "...": "s",
  "-": "t",
  "..-": "u",
  "...-": "v",
  ".--": "w",
  "-..-": "x",
  "-.--": "y",
  "--..": "z",
  ".----": "1",
  "..---": "2",
  "...--": "3",
  "....-": "4",
  ".....": "5",
  "-....": "6",
  "--...": "7",
  "---..": "8",
  "----.": "9",
  "-----": "0",
};

function decode(expr) {
  let array = expr.match(/.{1,10}/g);
  let result = "";
  for (let i = 0; i < array.length; i++) {
    if (array[i] === "**********") {
      result += " ";
    } else {
      let letter = array[i].replace(/10/g, ".").replace(/11/g, "-").replace(/0/g, "");
      result += MORSE_TABLE[letter];
    }
  }
  return result;
}


module.exports = {
    decode
}
